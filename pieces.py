# ================================
#    ASCII / UNICODE PIECES WITH COLOR
# ================================

# ANSI foreground colors
WHITE_FG = "\033[97m"   # bright white
BLACK_FG = "\033[30m"   # black
RESET    = "\033[0m"

# WHITE PIECES
WHITE_PAWN   = WHITE_FG + "♙" + RESET
WHITE_ROOK   = WHITE_FG + "♖" + RESET
WHITE_KNIGHT = WHITE_FG + "♘" + RESET
WHITE_BISHOP = WHITE_FG + "♗" + RESET
WHITE_QUEEN  = WHITE_FG + "♕" + RESET
WHITE_KING   = WHITE_FG + "♔" + RESET

# BLACK PIECES
BLACK_PAWN   = BLACK_FG + "♟" + RESET
BLACK_ROOK   = BLACK_FG + "♜" + RESET
BLACK_KNIGHT = BLACK_FG + "♞" + RESET
BLACK_BISHOP = BLACK_FG + "♝" + RESET
BLACK_QUEEN  = BLACK_FG + "♛" + RESET
BLACK_KING   = BLACK_FG + "♚" + RESET